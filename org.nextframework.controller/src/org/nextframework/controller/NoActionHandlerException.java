package org.nextframework.controller;

public class NoActionHandlerException extends Exception {

	public NoActionHandlerException() {
		super();
	}

	public NoActionHandlerException(String message, Throwable cause) {
		super(message, cause);
	}

	public NoActionHandlerException(String message) {
		super(message);
	}

	public NoActionHandlerException(Throwable cause) {
		super(cause);
	}

	private static final long serialVersionUID = 1L;

}
